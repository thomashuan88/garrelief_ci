    <p><b>Gar Relief</b> is a US-based charitable non-profit organization dedicated to supporting grassroots projects for people living in dire poverty in remote areas in Nangchen County. Our mission is to build local capacity and improve quality of life by advancing access to education and primary health care and promoting self-sustainability projects. Currently, our work is focused in Tajuk village and surrounding areas in Nangchen.</p>
